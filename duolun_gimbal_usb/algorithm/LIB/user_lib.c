void Kalman_init(easy_kalman_t *kf,fp32 p ,fp32 q, fp32 r)
{
	kf->p=p;
	kf->q=q;
	kf->r=r;
}

void Kalman_cacl(easy_kalman_t *kf,fp32 b,fp32 dt, fp32 z)
{
	kf->x_hat=kf->x+-b*dt;
	kf->p_hat=kf->p+kf->q;
	kf->k=kf->p_hat/(kf->p_hat+kf->r);
	kf->p=(1-kf->k)*kf->p_hat;
	kf->x=kf->x_hat+kf->k*(z-kf->x_hat);
}

