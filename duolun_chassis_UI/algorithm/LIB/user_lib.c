float Fabs(float a)
{
	// If a is negative, return its negation
	if (a < 0)
		return -a;
	// Otherwise, return a
	return a;
}
fp32 Calc(double x)
{
	if (x > 0)
		return sqrt(2.0 * x) / 2.0;
	else
		return -sqrt(-2.0 * x) / 2.0;
}
